//se usa para comparar distinas variables entre si o una variable con otra cosa

let a = 10;
console.log(a > 5); //cada vez que se usen operadores de comparacion, este nos devolvera un boolean
console.log (a >= 5); //mayor o igual
console.log (a < 5); //menor
console.log (a <= 5); // menor o igual

//OPERADORES DE IGUALDAD

console.log (a == 10);  //es exactamente igual
console.log (a != 10); // simbolo de ! antes de simbolo de = juntos, significa que son diferentes
console.log (a === "10"); //nos corrobora si tiene el mismo valor, tipo de dato
console.log (a !== "10"); // si es totalmente diferente